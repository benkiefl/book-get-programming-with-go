package main // declares package the code belongs to

import "fmt" // imports fmt (format) package

func main() {
	// declares main function
	// main function is always needed
	// execution flow starts here
	fmt.Println("你好 ሬᝪᖆlႧ")
}

/*
	package, import and func are _keywords_
	packages contain functions
		fmt : the package
		Println : a function of the package
	all code in Go is organized in packages
*/
